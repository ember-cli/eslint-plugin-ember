// Validate BCP 47 language tag (simplified)
function isValidLangTag(value) {
  if (!value || !value.trim()) {
    return false;
  }
  const parts = value.trim().toLowerCase().split('-');
  // Primary language subtag must be valid
  return COMMON_LANG_CODES.has(parts[0]);
}

/** @type {import('eslint').Rule.RuleModule} */
module.exports = {
  meta: {
    type: 'problem',
    docs: {
      description: 'require lang attribute on html element',
      category: 'Accessibility',
      strictGjs: true,
      strictGts: true,
      url: 'https://github.com/ember-cli/eslint-plugin-ember/tree/master/docs/rules/template-require-lang-attribute.md',
    },
    fixable: null,
    schema: [
      {
        type: 'object',
        properties: {
          validateValues: { type: 'boolean' },
        },
        additionalProperties: false,
      },
    ],
    messages: {
      missing: 'The <html> element must have a lang attribute.',
      empty: 'The <html> element must have a non-empty lang attribute.',
      invalid: 'The <html> element has an invalid lang value "{{value}}".',
    },
  },

  create(context) {
    const options = context.options[0] || {};
    const validateValues = options.validateValues !== false; // default true

    return {
      GlimmerElementNode(node) {
        if (node.tag !== 'html') {
          return;
        }

        const langAttr = node.attributes?.find((a) => a.name === 'lang');
        if (!langAttr) {
          context.report({ node, messageId: 'missing' });
          return;
        }

        if (validateValues && langAttr.value?.type === 'GlimmerTextNode') {
          const value = langAttr.value.chars;
          if (!value || !value.trim()) {
            context.report({ node, messageId: 'empty' });
          } else if (!isValidLangTag(value)) {
            context.report({ node, messageId: 'invalid', data: { value } });
          }
        }
      },
    };
  },
};
